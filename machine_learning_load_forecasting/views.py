from sklearn.neighbors import KNeighborsClassifier
from sklearn.linear_model import LogisticRegression


@api_view(['GET'])
def logistic_regression(request):
    iris = load_iris()
    x = iris.data
    y = iris.target
    x_new = [[3, 5, 4, 2], [5, 4, 3, 2]]
    log_reg = LogisticRegression()
    log_reg.fit(x, y)
    try:
        return JsonResponse({
            'observations': x_new,
            'predict': log_reg.predict(x_new).tolist()
        }, safe=False)
    except ValueError as e:
        return Response(e.args[0], status.HTTP_400_BAD_REQUEST)


@api_view(["GET"])
def iris_data(request):
    iris = load_iris()
    dic = {
        'data': iris.data.tolist(),
        'target_names': iris.target_names.tolist(),
        'data_shape': iris.data.shape,
        'target_shape': iris.target.shape,
        'feature_names': iris.feature_names,
        'target': iris.target.tolist()
    }
    try:
        return JsonResponse(dic, safe=False)
    except ValueError as e:
        return Response(e.args[0], status.HTTP_400_BAD_REQUEST)


@api_view(["GET"])
def knn(request):
    iris = load_iris()
    x = iris.data
    y = iris.target
    k_neighbors = KNeighborsClassifier(n_neighbors=1)
    k_neighbors.fit(x, y)
    x_new = [[3, 5, 4, 2], [5, 4, 3, 2]]
    try:
        return JsonResponse({
            'observations': x_new,
            'predict': k_neighbors.predict(x_new).tolist()
        }, safe=False)
    except ValueError as e:
        return Response(e.args[0], status.HTTP_400_BAD_REQUEST)


